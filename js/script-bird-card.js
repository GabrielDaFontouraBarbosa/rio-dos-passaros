function abrirLink(tipo) {
  const infoBox = document.getElementById('infoPassaro');
  const playerPiado = document.getElementById('playerPiado');

  const linkMap = {
    karaoke: infoBox.dataset.karaokeLink,
    youtube: infoBox.dataset.youtubeClip,
    piado: infoBox.dataset.piadoLink
  };

  const link = linkMap[tipo];

  if (!link) {
    alert("Link não disponível.");
    return;
  }

  if (tipo === "piado") {
    // Configura a fonte do áudio e toca
    playerPiado.src = link;
    playerPiado.play().catch(err => {
      console.error("Erro ao reproduzir o áudio:", err);
      alert("Não foi possível reproduzir o áudio. Verifique o console para detalhes.");
    });
  } else {
    // Para outros tipos, abre em nova aba como antes
    window.open(link, "_blank");
  }
}


function toggleLyrics() {
  var lyrics = document.getElementById("lyricsPassaro");
  var arrow = document.getElementById("lyricsArrow");
  
  if (lyrics.style.display === "none" || lyrics.style.display === "") {
    lyrics.style.display = "block";
    arrow.innerHTML = "&#9660;";
  } else {
    lyrics.style.display = "none";
    arrow.innerHTML = "&#9654;";
  }
}






