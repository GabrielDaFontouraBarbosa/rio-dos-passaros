;(function(){
  const carousel = document.querySelector('.birds-carousel');
  const track    = carousel.querySelector('.carousel-track');
  const btnLeft  = carousel.querySelector('.arrow.left');
  const btnRight = carousel.querySelector('.arrow.right');
  const items    = Array.from(track.children);
  const gap      = 12; // deve bater com o CSS

  let idx = 0;

  // largura de cada item + gap
  function getItemWidth() {
    const style = getComputedStyle(items[0]);
    const width = items[0].getBoundingClientRect().width;
    const margin = parseFloat(style.marginRight) || gap;
    return width + margin;
  }

  // quantos ícones cabem no viewport
  function getVisibleCount() {
    return Math.floor(carousel.clientWidth / getItemWidth());
  }

  // atualiza a posição do track
  function updateCarousel() {
    const x = -idx * getItemWidth();
    track.style.transform = `translateX(${x}px)`;
  }

  btnLeft.addEventListener('click', () => {
    idx = Math.max(0, idx - 1);      // ou use getVisibleCount() p/ pular página
    updateCarousel();
  });

  btnRight.addEventListener('click', () => {
    const maxIdx = items.length - getVisibleCount();
    idx = Math.min(maxIdx, idx + 1); // ou + getVisibleCount()
    updateCarousel();
  });

  // clique no botão do pássaro
  track.addEventListener('click', e => {
    const btn = e.target.closest('.bird-btn');
    if (!btn) return;
    mostrarInfo(btn.dataset.bird);
  });

  // reajuste no resize
  window.addEventListener('resize', () => {
    // garante que idx não ultrapasse o máximo válido
    const maxIdx = items.length - getVisibleCount();
    if (idx > maxIdx) idx = maxIdx;
    updateCarousel();
  });

  // iniciar sem delay
  updateCarousel();
})();



